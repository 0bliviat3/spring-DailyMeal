package com.human.VO;

public class FoodDetailVO {
	
	private int food_no;
	private int food_cnt;
	private String food_indate;
	
	public int getFood_no() {
		return food_no;
	}
	
	public void setFood_no(int food_no) {
		this.food_no = food_no;
	}
	
	public int getFood_cnt() {
		return food_cnt;
	}
	
	public void setFood_cnt(int food_cnt) {
		this.food_cnt = food_cnt;
	}
	
	public String getFood_indate() {
		return food_indate;
	}
	
	public void setFood_indate(String food_indate) {
		this.food_indate = food_indate;
	}
}
